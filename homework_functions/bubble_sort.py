def bubble_sort(numbers):
  # len - функция для получения длинны списка
    n = len(numbers)
    for i in range(n):
        # Флаг для оптимизации: если не было обменов, список отсортирован
        swapped = False
        for j in range(0, n - i - 1):
            # Если текущий элемент больше следующего, меняем их местами
            if numbers[j] > numbers[j + 1]:
                numbers[j], numbers[j + 1] = numbers[j + 1], numbers[j]
                swapped = True
        # Если не было обменов во внутреннем цикле, список отсортирован
        if not swapped:
            break
    return numbers


# 1. Запрашивает у пользователя список чисел через запятую
spisok = input("Введите числа через запятую: ")

# 2. Преобразуем строку в список чисел
# .strip() - убирает пробелы в начале и конце строки
# .split(",") - разбивает строки на отдельные части по указанному разделителю
try:
    numbers = [int(x.strip()) for x in spisok.split(",")]
except ValueError:
    print("Ошибка: Введите числа, разделенные запятыми.")
    exit()


# 3. Сортируем список чисел методом пузырька
sorted_numbers = bubble_sort(numbers)

# 4. Выводим отсортированный список
print("Отсортированный список:",", ".join(map(str, sorted_numbers))) # Преобразуем числа обратно в строки и соединяем через запятую
